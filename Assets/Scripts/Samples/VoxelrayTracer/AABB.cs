using System.Collections;
using System.Collections.Generic;
using UnityEngine;

public class AABB
{
    Vector3 m_Min;
    Vector3 m_Max;

    //----------------------------------------------------------------------------------------
    // 
    //----------------------------------------------------------------------------------------
    public Vector3 Min
    {
        get { return m_Min; }
        set { m_Min = value; }
    }
    //----------------------------------------------------------------------------------------
    // 
    //----------------------------------------------------------------------------------------
    public Vector3 Max
    {
        get { return m_Max; }
        set { m_Max = value; }
    }
    //----------------------------------------------------------------------------------------
    // 
    //----------------------------------------------------------------------------------------
    public AABB(Vector3 c, Vector3 l)
    {
        m_Min = c - l;
        m_Max = c + l;
    }
    //----------------------------------------------------------------------------------------
    // 
    //----------------------------------------------------------------------------------------
    public void Add(AABB o)
    {
        if (o.Min.x < m_Min.x)
            m_Min.x = o.Min.x;
        if (o.Min.y < m_Min.y)
            m_Min.y = o.Min.y;
        if (o.Min.z < m_Min.z)
            m_Min.z = o.Min.z;

        if (o.Max.x > m_Max.x)
            m_Max.x = o.Max.x;
        if (o.Max.y > m_Max.y)
            m_Max.y = o.Max.y;
        if (o.Max.z > m_Max.z)
            m_Max.z = o.Max.z;
    }
}
